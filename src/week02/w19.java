package week02;

public class w19 {
    public static void main(String[] args) {
        // for 문

        for(int i=0; i < 4; i++) { // 변수 i 값은 0 ~ 3 까지 반복
            System.out.println(i + "번째 출력"); // i 변수와 문자열 합치기
        }

        // 출력
        // 0번째 출력
        // 1번째 출력
        // 2번째 출력
        // 3번째 출력
    }
}
