package week02.operator;

public class w31 {
    public static void main(String[] args) {
        //길이가 3인 정수배열 선언
        int[] intArray = new int[3];

        // 배열이 초기화된 값을 출력해본다.
        for(int i=0; i<intArray.length; i++) { // .length 는 배열의 길이
            System.out.println(intArray[i]);
        }

        // 출력
        // 0
        // 0
        // 0
    }
}
