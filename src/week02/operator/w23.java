package week02.operator;

public class w23 {
    public static void main(String[] args) {
        // break 명령

        int number = 0;
        while(number < 3) {
            number++;
            if (number == 2) {
                break;  // 2일때 반복 중단
            }
            System.out.println(number + "출력");
        }

        // 출력
        // 1출력
    }
}
